'''
MOD 01: Python history and versions
'''


#===============================================================================
# December 1989: Python implementation started by Guido van Rossum as a successor to
#                the ABC programming language capable of exception handling and
#                interfacing with the Amoeba operating system.
#===============================================================================


#===============================================================================
# February 1991: Code is published (version 0.9.0) to alt.sources, with features like
#                classes with inheritance, exception handling, functions, and the core
#                datatypes of list, dict, str... Influenced from Modula-3 (exceptions
#                with else clause and module system)
#===============================================================================


#===============================================================================
# January 1994: Version 1.0 released, with functional programming tools like lambda,
#               map, filter and reduce
#===============================================================================


#===============================================================================
# 1994: Important milestone in the growth of Python's user base: comp.lang.python,
#       the primary discussion forum for Python, was formed
#===============================================================================


#===============================================================================
# 1995: Guido Van Rossum becomes Python's BDFL (Benevolent Dictator For Life)
#===============================================================================


#===============================================================================
# 1999: Version 1.5 released. Up to here several important features included, like
#       keyword arguments, complex numbers...
#===============================================================================


#===============================================================================
# 1999: Python gets DARPA funding (not first time it gets funding)
#===============================================================================


#===============================================================================
# 13 June 2000: PEP (Python Enhancement Proposal) system established, to let the
#               community submit extensions and new features. Even Guido uses it
#===============================================================================


#===============================================================================
# 16 October 2000: Python 2.0 was released, with many major new features including a
#                  full garbage collector and support for unicode. Core development
#                  process shifted to a more transparent and community-backed process
#===============================================================================


#===============================================================================
# 2000 / 2001: Versions 1.6.1 and 2.0.1 (partially overlapped), first ever to have
#              GPL-compatible license
#===============================================================================


#===============================================================================
# 6 March 2001: Creation of the Python Software Foundation (PSF), is a non-profit
#               organization devoted to the Python programming language. A new
#               Python Software Foundation License is used in version 2.1.
#===============================================================================


#===============================================================================
# 2001 / 2002: Versions 2.2 and 2.2.1 (and 2.3 in 2002-2003) introduction of new-style
#              classes, descriptors, super, new MRO
#===============================================================================


#===============================================================================
# 3 December 2008: Python 3.0, a major, backwards-incompatible release, was released
#                  after a long period of testing. Many of its major features have
#                  also been backported to the backwards-compatible Python 2.6 and 2.7.
#                  Backwards compatibility mainly broken because of print statement has
#                  been replaced with a print() function, massive usage of iterators
#                  instead of lists (as functions result)... and the most important one;
#                  all text (str) is Unicode and encoded text is binary data (bytes)
#===============================================================================


#===============================================================================
# 2010: Versions 2.6 and 2.7 with backports of several Py3k features to 2.X series:
#       collections, print, argparse, optimizations... as well as bugfixing. No more
#       new features will be added to 2.X series.
#===============================================================================


#===============================================================================
# November 2011: Python is the third most popular language on GitHub.com and it is
#                consistently in Top10 according to jobs postings and to the TIOBE
#                Programming Community Index.
#===============================================================================


#===============================================================================
# 7th March 2016: Last versions released: 3.5.1 and 2.7.11
#===============================================================================


