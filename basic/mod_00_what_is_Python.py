#===============================================================================
# From Wikipedia ( http://en.wikipedia.org/wiki/Python_(programming_language) ):
#
#     Python is a general-purpose, high-level programming language whose design
#     philosophy emphasizes code readability. Python's syntax allows programmers
#     to express concepts in fewer lines of code than would be possible in languages
#     such as C, and the language provides constructs intended to enable clear
#     programs on both a small and large scale.
#
#     Python supports multiple programming paradigms, including object-oriented,
#     imperative and functional programming styles. It features a fully dynamic
#     type system and automatic memory management and has a large and comprehensive
#     standard library.
#
#===============================================================================


#===============================================================================
# TO SUM UP
#  - quick development
#  - simple, readable, easy to learn syntax
#  - general purpose
#  - interpreted (not compiled)
#  - object-oriented
#  - high-level
#  - dynamic semantics (aka execution semantics)
#  - fully dynamic typing
#  - dynamic binding
#  - low programs manteinance cost
#  - modularity and code reuse
#  - no licensing costs
#  - extensive standard library, "batteries included"
#  - imperative and functional programming
#  - automatic memory management
